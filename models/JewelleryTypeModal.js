const mongoose = require("mongoose");

const JewelleryTypeSchema = new mongoose.Schema(
  {
    type: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

module.exports = mongoose.model("JewelleryType", JewelleryTypeSchema);
